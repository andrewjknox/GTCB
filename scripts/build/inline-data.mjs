#!/usr/bin/env node
// Build step — inlines data/plan.json + data/summary/* into site/data.js so the
// dashboard renders from file:// where fetch() of relative URLs is unavailable.
// Same dependency-free pattern as minify.mjs. index.html loads data.js before
// app.min.js; app.js prefers fetch when served over http(s) so the inline copy
// can never shadow fresh JSON. Exit 0 = built, 2 = failed.

import { readFileSync, writeFileSync, statSync, existsSync } from "node:fs";

const OUT = "site/data.js";

function readJSON(path) {
  let text;
  try {
    text = readFileSync(path, "utf8");
  } catch (e) {
    console.error(`inline-data: cannot read ${path}: ${e.message}`);
    process.exit(2);
  }
  try {
    return JSON.parse(text);
  } catch (e) {
    console.error(`inline-data: cannot parse ${path}: ${e.message}`);
    process.exit(2);
  }
}

const plan = readJSON("data/plan.json");
const index = readJSON("data/summary/index.json");

if (!Array.isArray(index.weeks)) {
  console.error("inline-data: data/summary/index.json has no weeks array");
  process.exit(2);
}

const summaries = {};
for (const week of index.weeks) {
  const summary = readJSON(`data/summary/${week}.json`);
  summaries[summary.iso_week] = summary;
}

// pace-model reference for the course page's PREDICTED planner mode
// (data/refs/pace-model.json, generated by fit-grade-curve.mjs)
const paceModel = readJSON("data/refs/pace-model.json");

// weekly debrief reports (data/reports/, generated on demand by report-agent +
// scripts/report.mjs) — optional: absent until the first report is generated
const reports = {};
if (existsSync("data/reports/index.json")) {
  const reportIndex = readJSON("data/reports/index.json");
  if (!Array.isArray(reportIndex.weeks)) {
    console.error("inline-data: data/reports/index.json has no weeks array");
    process.exit(2);
  }
  for (const week of reportIndex.weeks) {
    const report = readJSON(`data/reports/${week}.json`);
    reports[report.iso_week] = report;
  }
}

const banner =
  "/* Generated by scripts/build/inline-data.mjs — inline copy of data/plan.json + data/summary/*\n" +
  "   so the dashboard renders from file:// where fetch() is unavailable. Do not edit by hand. */\n";
const payload = JSON.stringify({ plan, index, summaries, paceModel, reports });

try {
  writeFileSync(OUT, `${banner}window.GTCB_DATA = ${payload};\n`);
} catch (e) {
  console.error(`inline-data: cannot write ${OUT}: ${e.message}`);
  process.exit(2);
}

console.log(`inline-data: ${OUT} ${statSync(OUT).size} B (${index.weeks.length} weeks inlined)`);
process.exit(0);
